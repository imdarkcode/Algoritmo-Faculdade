print("==============================")
valor = int(input("Informe uma número: "))
if valor >= 0 and valor <= 9:
    if valor == 0:
        print("Zero")
    elif valor == 1:
        print("Um")
    elif valor == 2:
        print("Dois")
    elif valor == 3:
        print("Três")
    elif valor == 4:
        print("Quatro")
    elif valor == 5:
        print("Cinco")
    elif valor == 6:
        print("Seis")
    elif valor == 7:
        print("Sete")
    elif valor == 8:
        print("Oito")
    elif valor == 9:
        print("Nove")
else:
    print("Valor inválido")
print("==============================")