print("==============================")
valor = int(input("Informe uma número: "))
if valor >= 1 and valor <= 10:
    if valor == 1:
        print("I")
    elif valor == 2:
        print("II")
    elif valor == 3:
        print("III")
    elif valor == 4:
        print("IV")
    elif valor == 5:
        print("V")
    elif valor == 6:
        print("VI")
    elif valor == 7:
        print("VII")
    elif valor == 8:
        print("VIII")
    elif valor == 9:
        print("IX")
    elif valor == 10:
        print("X")
else:
    print("Valor inválido")
print("==============================")